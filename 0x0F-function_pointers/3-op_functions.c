#include "3-calc.h"

/**
 * op_add- function to add
 * @a:operand
 * @b:operand
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- function to subtract
 * @a:operator
 * @b:operator
 * Return:sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- function to multiply
 * @a:operand
 * @b:operand
 * Return:sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div- function to divide
 * @a:operand
 * @b:operand
 * Return:division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- function to get remainder
 * @a:operand
 * @b:operand
 * Return:remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
